// Compressed format: hex digits per pixel, rows separated by |
// 0=transparent, other digits index into palette
const P = 3;

// Decode compressed frame string to 2D array
function decodeFrame(s) {
  return s.split('|').map(row => [...row].map(c => parseInt(c, 16)));
}

