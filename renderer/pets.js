// ── Renderer ─────────────────────────────────────────────────────────
const canvas = document.getElementById('capybara');
const ctx = canvas ? canvas.getContext('2d') : null;

const petKeys = Object.keys(pets);
// Unlocked pets set — chick is the starter pet, always unlocked
let unlockedPets = new Set(['chick']);
let currentPet = localStorage.getItem('pet') || 'chick';
if (!pets[currentPet] || !unlockedPets.has(currentPet)) currentPet = 'chick';
let currentState = 'idle';
let frameIndex = 0;
let animTimer = null;

function drawFrame(frame, palette) {
  if (!ctx) return;
  ctx.clearRect(0, 0, canvas.width, canvas.height);
  for (let y = 0; y < frame.length; y++) {
    for (let x = 0; x < frame[y].length; x++) {
      const c = palette[frame[y][x]];
      if (c) {
        ctx.fillStyle = c;
        ctx.fillRect(x * P, y * P, P, P);
      }
    }
  }
}

function animate() {
  const pet = pets[currentPet];
  const frames = pet[currentState];
  if (!frames) return;
  frameIndex = (frameIndex + 1) % frames.length;
  drawFrame(frames[frameIndex], pet.palette);
}

function setPetState(state) {
  if (!pets[currentPet][state]) return;
  if (state === currentState) return;
  currentState = state;
  frameIndex = 0;
  const pet = pets[currentPet];
  drawFrame(pet[currentState][0], pet.palette);
  clearInterval(animTimer);
  const speeds = { idle: 800, running: 250, waiting: 600 };
  animTimer = setInterval(animate, speeds[state] || 600);
}

function switchPet(key) {
  if (key && pets[key] && unlockedPets.has(key)) {
    currentPet = key;
  } else {
    // Cycle through unlocked pets only
    const unlocked = petKeys.filter(k => unlockedPets.has(k));
    if (unlocked.length === 0) return currentPet;
    const idx = unlocked.indexOf(currentPet);
    currentPet = unlocked[(idx + 1) % unlocked.length];
  }
  localStorage.setItem('pet', currentPet);
  frameIndex = 0;
  const pet = pets[currentPet];
  drawFrame(pet[currentState][0], pet.palette);
  clearInterval(animTimer);
  const speeds = { idle: 800, running: 250, waiting: 600 };
  animTimer = setInterval(animate, speeds[currentState] || 600);
  return currentPet;
}

function setUnlockedPets(keys) {
  unlockedPets = new Set(['chick', ...keys]); // chick always unlocked
  // If current pet got locked, switch to first unlocked
  if (!unlockedPets.has(currentPet)) {
    const unlocked = petKeys.filter(k => unlockedPets.has(k));
    currentPet = unlocked[0] || 'chick';
    localStorage.setItem('pet', currentPet);
    frameIndex = 0;
    const pet = pets[currentPet];
    if (ctx) drawFrame(pet[currentState][0], pet.palette);
  }
}

function getAllPets() {
  const result = {};
  for (const [key, pet] of Object.entries(pets)) {
    result[key] = { name: pet.name, rarity: pet.rarity, palette: pet.palette, idle: pet.idle };
  }
  return result;
}

// Click canvas to cycle pet
if (canvas) {
  canvas.style.cursor = 'pointer';
  canvas.title = 'Click to switch pet';
  canvas.addEventListener('click', (e) => {
    e.stopPropagation();
    const name = switchPet();
    // Brief tooltip-like feedback
    canvas.title = pets[name].name;
  });
}

// Init
if (ctx) {
  const pet = pets[currentPet];
  drawFrame(pet.idle[0], pet.palette);
  animTimer = setInterval(animate, 800);
}

// Exports
window.setPetState = setPetState;
window.switchPet = switchPet;
window.setUnlockedPets = setUnlockedPets;
window.getAllPets = getAllPets;
window.getPetList = () => petKeys.map(k => ({ key: k, name: pets[k].name, rarity: pets[k].rarity, unlocked: unlockedPets.has(k) }));
window.getCurrentPet = () => currentPet;
